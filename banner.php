$(document).ready(function() {
    // Khi click vào tab
    $('.tab-item').on('click', function() {
        $('.tab-item').removeClass('active');
        $(this).addClass('active');

        const tabId = $(this).data('tab');
        $('.tab-panel').addClass('d-none');
        $('#' + tabId).removeClass('d-none');
    });


    // cái này của tab thư viện
    $(document).ready(function() {
        const scrollContainer = $('.tabs-scroll');
        const scrollAmount = 100;

        $('.scroll-button.prev').on('click', function() {
            scrollContainer.animate({
                scrollLeft: '-=' + scrollAmount
            }, 200);
        });

        $('.scroll-button.next').on('click', function() {
            scrollContainer.animate({
                scrollLeft: '+=' + scrollAmount
            }, 200);
        });

        $('.tab-item').on('click', function() {
            $('.tab-item').removeClass('active');
            $(this).addClass('active');

            const tabId = $(this).data('tab');
            $('.tab-panel').addClass('d-none');
            $('#' + tabId).removeClass('d-none');

            // 👉 Tự động scroll tab vào giữa nếu bị che
            const container = $('.tabs-scroll');
            const tab = $(this);

            const containerOffset = container.offset().left;
            const tabOffset = tab.offset().left;
            const containerScroll = container.scrollLeft();
            const containerWidth = container.outerWidth();
            const tabWidth = tab.outerWidth();

            const scrollTo = containerScroll + (tabOffset - containerOffset) - (
                containerWidth /
                2) + (tabWidth / 2);

            container.animate({
                scrollLeft: scrollTo
            }, 300);
        });
    });


    // Khi click nút next của tab thư viện
    $('.scroll-button.next').on('click', function() {
        const $current = $('.tab-item.active');
        const $next = $current.next('.tab-item');
        if ($next.length) {
            $current.removeClass('active');
            $next.addClass('active');

            const tabId = $next.data('tab');
            $('.tab-panel').addClass('d-none');
            $('#' + tabId).removeClass('d-none');
        }
    });

    // Khi click nút prev của tab thư viện
    $('.scroll-button.prev').on('click', function() {
        const $current = $('.tab-item.active');
        const $prev = $current.prev('.tab-item');
        if ($prev.length) {
            $current.removeClass('active');
            $prev.addClass('active');

            const tabId = $prev.data('tab');
            $('.tab-panel').addClass('d-none');
            $('#' + tabId).removeClass('d-none');
        }
    });
});




